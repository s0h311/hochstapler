import { Player } from "@/types";

export function getRandomPlayer(
  totalPlayers: number,
  totalImposters: number
): Player[] {
  let players = new Array<Player>(totalPlayers);

  players.fill("regular", 0, totalPlayers);

  let determinedImpostersCount = 0;

  while (determinedImpostersCount < totalImposters) {
    const randomIndex = Math.floor(Math.random() * totalPlayers);

    const player = players[randomIndex];

    if (player !== "imposter") {
      players[randomIndex] = "imposter";
      determinedImpostersCount++;
    }
  }

  return players;
}

export function getRandomWord(): string {
  const randomIndex = Math.floor(Math.random() * words.length);
  return words[randomIndex];
}

